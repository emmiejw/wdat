            <div id="drugs" class="col s12">
                <p class="flow-text indigo-text text-darken-4">Drug Testing</p>
                <p>Breath can be tested for alcohol using handheld devices which provide immediate results. These devices are specific to alcohol and can gauge blood alcohol content (BAC) by measuring deep lung air. This type of testing can accurately determine whether a person has recently consumed alcohol or is currently over the legal or pre-determined limit. These instruments are simple to use, accurate and robust and have a variety of functions including the ability to test passive, direct or liquid samples as well as breath expelled into a container.
                </p>
                <p><b>How it works:</b><br>
                    When alcohol is consumed it is absorbed into the bloodstream. When the blood reaches the lungs a small amount of alcohol (in proportion to the concentration in the blood) transfers into the alveoli. This is then passed out of the body when the person exhales. A breathalyser is then able to measure this concentration and give a reading.
                </p>
